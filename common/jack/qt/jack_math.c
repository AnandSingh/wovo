double Evceil(double x)
{
	return ceil(x);
}

/*---------------------------------------------------------------------------*/
double Evfloor (double x)
{
	return floor(x);
}
/*---------------------------------------------------------------------------*/
double Evfabs (double x)
{
	return floor(x);
}
